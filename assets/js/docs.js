function subirFotoUser() {
	//información del formulario
	var formData = new FormData($("#frmfoto")[0]);
	//hacemos la petición ajax
	$.ajax({
		url: get_base_url() + "conductor/Perfil/subir_foto_user_ajax",
		type: 'POST',
		// Form data
		//datos del formulario
		data: formData,
		//necesario para subir archivos via ajax
		cache: false,
		contentType: false,
		processData: false,
		//mientras enviamos el archivo
		beforeSend: function () {
			swal({
				title: "Aviso!",
				text: "Subiendo archivo.",
				imageUrl: get_base_url() + 'assets/img/loaderdocs.gif',
				showConfirmButton: false
			});
		},
		//una vez finalizado correctamente
		success: function () {
			swal({
				title: "Exito!",
				type: "success",
				text: "La foto se ha subido correctamente",
				showConfirmButton: false
			});
			location.reload();
		},
		//si ha ocurrido un error
		error: function () {
			swal({
				title: "Error!",
				type: "error",
				text: "Ha ocurrido un error.",
				showConfirmButton: false
			});
		}
	});
}
